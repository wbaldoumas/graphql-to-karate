{
    public const char OpenBrace = '{';
    
    public const char CloseBrace = '}';

    public const char Space = ' ';

    public const char Comma = ',';

    public const string Indent = "  ";

    public const char OpenBracket = '[';

    public const char CloseBracket = ']';
}